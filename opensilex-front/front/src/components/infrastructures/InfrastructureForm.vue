<template>
  <b-modal ref="modalRef" @ok.prevent="validate">
    <template v-slot:modal-ok>{{$t('component.common.ok')}}</template>
    <template v-slot:modal-cancel>{{$t('component.common.cancel')}}</template>

    <template v-slot:modal-title>{{title}}</template>
    <ValidationObserver ref="validatorRef">
      <b-form>
        <!-- URI -->
        <b-form-group :label="$t('component.user.user-uri') + ':'" label-for="uri">
          <ValidationProvider vid="autogenerated">
            <b-form-checkbox
              v-if="!editMode"
              id="autogenerated"
              v-model="uriGenerated"
              name="autogenerated"
            >{{$t('component.common.autogenerated-uri')}}</b-form-checkbox>
          </ValidationProvider>
          <ValidationProvider
            name="uri"
            rules="required_if:autogenerated,false|url"
            v-slot="{ errors }"
          >
            <b-form-input
              id="uri"
              v-model="form.uri"
              :disabled="uriGenerated"
              type="text"
              required
              :placeholder="$t('component.common.autogenerated-uri')"
            ></b-form-input>
            <div class="error-message alert alert-danger">{{ errors[0] }}</div>
          </ValidationProvider>
        </b-form-group>
        <!-- Name -->
        <b-form-group :label="$t('component.common.name') + ':'" label-for="name" required>
          <ValidationProvider
            :name="$t('component.common.name')"
            rules="required"
            v-slot="{ errors }"
          >
            <b-form-input
              id="name"
              v-model="form.name"
              type="text"
              required
              :placeholder="$t('component.common.form-name-placeholder')"
            ></b-form-input>
            <div class="error-message alert alert-danger">{{ errors[0] }}</div>
          </ValidationProvider>
        </b-form-group>
        <!-- Parent -->
        <b-form-group :label="$t('component.common.parent') + ':'" label-for="parent" required>
          <treeselect
            id="parent"
            :options="parentOptions"
            :placeholder="$t('component.infrastructure.form-parent-placeholder')"
            v-model="form.parent"
          />
        </b-form-group>
      </b-form>
    </ValidationObserver>
  </b-modal>
</template>

<script lang="ts">
import { Component, Prop } from "vue-property-decorator";
import Vue from "vue";
import VueRouter from "vue-router";
import {
  InfrastructureCreationDTO,
  InfrastructureGetDTO,
  ResourceTreeDTO,
  InfrastructuresService
} from "opensilex-core/index";
import HttpResponse, { OpenSilexResponse } from "../../lib/HttpResponse";

@Component
export default class InfrastructureForm extends Vue {
  $opensilex: any;
  $store: any;
  $router: VueRouter;
  $i18n: any;
  $t: any;
  service: InfrastructuresService;

  @Prop()
  public defaultParent: InfrastructureGetDTO;

  @Prop()
  parentOptions: Array<any>;

  get user() {
    return this.$store.state.user;
  }

  uriGenerated = true;

  form: InfrastructureCreationDTO = {
    uri: "",
    type: "",
    name: "",
    parent: null,
    users: []
  };

  title = "";

  editMode = false;

  clearForm() {
    let parentURI = null;
    if (this.defaultParent) {
      parentURI = this.defaultParent.uri;
    }
    this.form = {
      uri: "",
      type: "",
      name: "",
      parent: parentURI,
      users: []
    };
  }

  showCreateForm(parentURI) {
    this.clearForm();
    this.form.parent = parentURI;
    this.editMode = false;
    this.title = this.$t("component.infrastructure.add").toString();
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
  }

  showEditForm(form: InfrastructureCreationDTO) {
    this.form = form;
    this.editMode = true;
    this.title = this.$t("component.infrastructure.update").toString();
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
  }

  hideForm() {
    let modalRef: any = this.$refs.modalRef;
    modalRef.hide();
  }

  onValidate() {
    return new Promise((resolve, reject) => {
      if (this.editMode) {
        this.$emit("onUpdate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      } else {
        return this.$emit("onCreate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      }
    });
  }

  validate() {
    let validatorRef: any = this.$refs.validatorRef;
    validatorRef.validate().then(isValid => {
      if (isValid) {
        if (this.uriGenerated && !this.editMode) {
          this.form.uri = null;
        }

        this.onValidate()
          .then(() => {
            this.$nextTick(() => {
              let modalRef: any = this.$refs.modalRef;
              modalRef.hide();
            });
          })
          .catch(error => {
            if (error.status == 409) {
              console.error("User already exists", error);
              this.$opensilex.errorHandler(
                error,
                this.$i18n.t("component.user.errors.user-already-exists")
              );
            } else {
              this.$opensilex.errorHandler(error);
            }
          });
      }
    });
  }

  passwordValidationRule() {
    if (this.editMode) {
      return "";
    } else {
      return "required";
    }
  }
}
</script>

