<template>
  <b-modal ref="modalRef" @ok.prevent="validate" size="lg">
    <template v-slot:modal-title>{{title}}</template>
    <b-form ref="formRef">
      <b-form-group label="Group URI:" label-for="uri">
        <b-form-checkbox
          v-if="!editMode"
          id="autogenerated"
          v-model="uriGenerated"
          name="autogenerated"
        >Auto-generate URI</b-form-checkbox>
        <b-form-input
          id="uri"
          v-model="form.uri"
          :disabled="uriGenerated"
          type="text"
          required
          placeholder="Auto-generated URI"
        ></b-form-input>
      </b-form-group>
      <b-form-group label="Name:" label-for="name" required>
        <b-form-input
          id="name"
          v-model="form.name"
          type="text"
          required
          placeholder="Enter group name"
        ></b-form-input>
      </b-form-group>
      <b-form-group label="Description:" label-for="description">
        <b-form-textarea
          id="description"
          v-model="form.description"
          placeholder="Enter description"
          autocomplete="no"
          rows="3"
        ></b-form-textarea>
      </b-form-group>
      <b-input-group class="mt-3 mb-3" size="sm">
        <b-input-group>
          <b-form-input v-model="filterPattern" debounce="300" placeholder="Filter users"></b-form-input>
          <template v-slot:append>
            <b-btn :disabled="!filterPattern" variant="primary" @click="filterPattern = ''">
              <font-awesome-icon icon="times" size="sm" />
            </b-btn>
          </template>
        </b-input-group>
      </b-input-group>
      <b-table
        ref="tableRef"
        striped
        hover
        small
        :items="loadData"
        :fields="fields"
        :sort-by.sync="sortBy"
        :sort-desc.sync="sortDesc"
        no-provider-paging
      >
        <template v-slot:cell(selected)="data">
          <b-form-checkbox
            v-model="selectedUsersId[data.item.uri]"
            @change="toggleUserSelection(data.item)"
          ></b-form-checkbox>
        </template>

        <template v-slot:cell(email)="data">
          <a :href="'mailto:' + data.item.email">{{ data.item.email }}</a>
        </template>

        <template v-slot:cell(admin)="data">
          <small v-if="data.item.admin">Admin</small>
          <small v-if="!data.item.admin">Regular user</small>
        </template>
      </b-table>
      <b-pagination
        v-model="currentPage"
        :total-rows="totalRow"
        :per-page="pageSize"
        @change="refresh()"
      ></b-pagination>
      <b-table striped hover small :items="selectedUsers" :fields="fields">
        <template v-slot:cell(selected)="data">
          <b-form-checkbox
            v-model="selectedUsersId[data.item.uri]"
            @change="toggleUserSelection(data.item)"            
          ></b-form-checkbox>
        </template>

        <template v-slot:cell(email)="data">
          <a :href="'mailto:' + data.item.email">{{ data.item.email }}</a>
        </template>

        <template v-slot:cell(uri)="data">
          <a class="uri-info">
            <small>{{ data.item.uri }}</small>
          </a>
        </template>

        <template v-slot:cell(admin)="data">
          <small v-if="data.item.admin">Admin</small>
          <b-form-select v-if="!data.item.admin" size="sm"></b-form-select>
        </template>
      </b-table>
      <!-- <b-pagination v-model="currentPage" :total-rows="totalRow" :per-page="pageSize"></b-pagination> -->
    </b-form>
  </b-modal>
</template>

<script lang="ts">
import { Component, Prop } from "vue-property-decorator";
import Vue from "vue";
import VueRouter from "vue-router";
import {
  UserCreationDTO,
  GroupCreationDTO,
  UserService,
  UserGetDTO
} from "opensilex-rest/index";
import HttpResponse, { OpenSilexResponse } from "../../lib/HttpResponse";

@Component
export default class GroupForm extends Vue {
  $opensilex: any;
  $store: any;
  $router: VueRouter;

  get user() {
    return this.$store.state.user;
  }

  uriGenerated = true;

  form: GroupCreationDTO = {
    uri: "",
    name: "",
    description: "",
    userProfiles: []
  };

  title = "Add group";

  editMode = false;

  clearForm() {
    this.form = {
      uri: "",
      name: "",
      description: "",
      userProfiles: []
    };
  }

  refresh() {
    let tableRef: any = this.$refs.tableRef;
    tableRef.refresh();
  }

  currentPage: number = 1;
  pageSize = 5;
  totalRow = 0;
  sortBy = "firstName";
  sortDesc = false;

  private filterPatternValue: any = "";
  set filterPattern(value: string) {
    this.filterPatternValue = value;
    this.refresh();
  }

  get filterPattern() {
    return this.filterPatternValue;
  }

  loadData() {
    let service: UserService = this.$opensilex.getService(
      "opensilex.UserService"
    );

    let orderBy = [];
    if (this.sortBy) {
      let orderByText = this.sortBy + "=";
      if (this.sortDesc) {
        orderBy.push(orderByText + "desc");
      } else {
        orderBy.push(orderByText + "asc");
      }
    }

    return service
      .searchUsers(
        this.user.getAuthorizationHeader(),
        this.filterPattern,
        orderBy,
        this.currentPage - 1,
        this.pageSize
      )
      .then((http: HttpResponse<OpenSilexResponse<Array<UserGetDTO>>>) => {
        this.totalRow = http.response.metadata.pagination.totalCount;
        this.pageSize = http.response.metadata.pagination.pageSize;
        setTimeout(() => {
          this.currentPage = http.response.metadata.pagination.currentPage + 1;
        }, 0);

        this.$router
          .push({
            path: this.$route.fullPath,
            query: {
              filterPattern: encodeURI(this.filterPattern),
              sortBy: encodeURI(this.sortBy),
              sortDesc: "" + this.sortDesc,
              currentPage: "" + this.currentPage,
              pageSize: "" + this.pageSize
            }
          })
          .catch(function() {});

        return http.response.result;
      })
      .catch(this.$opensilex.errorHandler);
  }

  fields = [
    {
      key: "selected",
      label: ""
    },
    {
      key: "firstName",
      sortable: true
    },
    {
      key: "lastName",
      sortable: true
    },
    {
      key: "email",
      sortable: true
    },
    {
      key: "admin",
      sortable: true,
      label: "Role"
    }
  ];

  selectedUsersId: any = {};

  selectedUsers: any = [];

  toggleUserSelection(user) {
    if (!this.selectedUsersId[user.uri]) {
      this.selectedUsers.push(user);
    } else {
      this.selectedUsers = this.selectedUsers.filter((element) => {
        return element.uri != user.uri;
      })
      delete this.selectedUsersId[user.uri];
    }
  }

  showCreateForm() {
    this.clearForm();
    this.editMode = false;
    this.title = "Add group";
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
  }

  showEditForm(form: GroupCreationDTO) {
    this.form = form;
    this.editMode = true;
    this.title = "Update group";
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
  }

  hideForm() {
    let modalRef: any = this.$refs.modalRef;
    modalRef.hide();
  }

  onValidate() {
    return new Promise((resolve, reject) => {
      if (this.editMode) {
        this.$emit("onUpdate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      } else {
        return this.$emit("onCreate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      }
    });
  }

  validate() {
    let formRef: any = this.$refs.formRef;
    if (formRef.checkValidity()) {
      if (this.uriGenerated && !this.editMode) {
        this.form.uri = null;
      }

      this.onValidate()
        .then(() => {
          this.$nextTick(() => {
            let modalRef: any = this.$refs.modalRef;
            modalRef.hide();
          });
        })
        .catch(error => {
          if (error.status == 409) {
            // TODO display error message group already exists
            console.error("TODO display error message user already exists");
          } else {
            this.$opensilex.errorHandler(error);
          }
        });
    } else {
      // TODO validation error management
      console.error("TODO validation error management");
    }
  }
}
</script>

